let a = prompt("Número 1: ")
let b = prompt("Número 2: ")
let c = prompt("Número 2: ")

if ( ( a >= b ) && ( (a >= c) ) ) {
    alert("El mayor es: " + a)
} else if ( ( b >= a ) && ( b >= c ) ) {
    alert("El mayor es: " + b)
} else if ( ( c >= a ) && ( c >= b ) ) {
    alert("El mayor es: " + c)
}
